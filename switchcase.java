import java.util.*;
class switchcase{
public static void main(String args[]){
Scanner n=new Scanner(System.in);
int a=n.nextInt();
switch(a){
 	case 1: System.out.println("hello");
		break;
	case 2: System.out.println("hey guys");
		break;
	case 3: System.out.println("Hey man");
		break;
	case 4: System.out.println("chandra");
		break;
	default: System.out.println("Default");
		break;
}
}
}

       